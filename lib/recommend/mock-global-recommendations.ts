import type {
  RecommendationAgeBand,
  RecommendationCategory,
  RecommendationCurrency,
  RecommendationGender,
  RecommendationInterest,
  RecommendationItem,
} from "@/lib/recommend/recommendations-ui-types";

function iso(daysAgo: number): string {
  const d = new Date();
  d.setDate(d.getDate() - daysAgo);
  return d.toISOString();
}

function item(
  id: string,
  mediaName: string,
  city: string,
  country: string,
  globalCountryCode: string,
  aiMatchScore: number,
  aiReason: string,
  monthlyPrice: number,
  currency: RecommendationCurrency,
  specSize: string,
  specResolution: string,
  category: RecommendationCategory,
  targetAges: RecommendationAgeBand[],
  targetGender: RecommendationGender,
  interests: RecommendationInterest[],
  daysAgo: number,
): RecommendationItem {
  return {
    id,
    mediaName,
    imageUrl: `https://images.unsplash.com/${id}?auto=format&fit=crop&w=1200&q=80`,
    city,
    country,
    globalCountryCode,
    aiMatchScore,
    aiReason,
    monthlyPrice,
    currency,
    specSize,
    specResolution,
    category,
    targetAges,
    targetGender,
    interests,
    updatedAt: iso(daysAgo),
  };
}

